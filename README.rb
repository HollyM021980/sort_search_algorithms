# Sorting and search algorithms

Exercise as a part of the [GA Web Development Immersive] (https://generalassemb.ly/education/web-development-immersive) program to learn about search and sort algorithms. Most written in both ruby and javascript. This was writtin paired with another WDI student.

## Ruby
- cd to ruby directory.
- Run `ruby sorting.rb`

To run tests
- cd to ruby directory
- Run `rspec spec`


## Javascript

- Run `bower install` in this directory to install required JS libraries.
- Run `ruby -run -e httpd -- -p 5000 .` and then open [http://localhost:5000](http://localhost:5000) in your browser to see the contents of this folder.
- Use console to see results of running the javascript functions.

To run tests

- cd to javascript directory
- Run `npm install -g karma karma-cli` from anywhere (once to install karma)
- Run `npm install` in this directory
- Run `karma start` in the test directory

